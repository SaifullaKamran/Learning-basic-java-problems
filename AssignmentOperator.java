package com.it.bd.JavaBasics;

public class AssignmentOperator {
	//use of assignment operator

	public static void main(String[] args) {
		int x = 10;
		int y = 5;
		
		x+=y;  //x=x+y
		System.out.println("Value of x: "+x);
		x-=y;  //x=x-y
		System.out.println("Value of x: "+x);
		x*=y;  //x=x*y
		System.out.println("Value of x: "+x);
		x/=y;  //x=x/y
		System.out.println("Value of x: "+x);
		x%=y;  //x=x%y
		System.out.println("Value of x: "+x);
		

	}

}
